  bu_code                text NOT NULL DEFAULT 'REACT'::text, -- divergence from enum-based bu_code
  inquiry_id             bigint, -- not a DB-level FK currently
  quote_id               bigint, -- not a DB-level FK currently
  title                  text NOT NULL,
  client_company         text NOT NULL DEFAULT ''::text,
  client_address         text DEFAULT ''::text,
  client_representative  text DEFAULT ''::text,
  client_email           text DEFAULT ''::text,
  client_phone           text DEFAULT ''::text,
  task_description       text DEFAULT ''::text,
  deliverables           text DEFAULT '편집이 완료된 최종 마스터 파일 1종'::text,
  shooting_date          date,
  delivery_date          date,
  release_date           date,
  total_amount           numeric NOT NULL DEFAULT 0,
  vat_type               text NOT NULL DEFAULT 'exclusive'::text,
  deposit_rate           integer NOT NULL DEFAULT 20,
  deposit_amount         numeric NOT NULL DEFAULT 0,
  balance_rate           integer NOT NULL DEFAULT 80,
  balance_amount         numeric NOT NULL DEFAULT 0,
  deposit_condition      text DEFAULT '계약 체결 후 7일 이내 지급'::text,
  balance_condition      text DEFAULT '릴리즈 예정일로부터 30일 이내 지급'::text,
  free_revision_count    integer NOT NULL DEFAULT 2,
  penalty_rates          jsonb NOT NULL DEFAULT
    '[{"rate": 30, "label": "계약 체결 후 ~ 촬영 7일 전"},
      {"rate": 50, "label": "촬영 예정일 기준 7일 이내"},
      {"rate": 100, "label": "촬영 완료 이후"}]'::jsonb,
  contract_date          date,
  status                 text NOT NULL DEFAULT 'draft'::text,
  sent_at                timestamptz,
  created_at             timestamptz NOT NULL DEFAULT now(),
  updated_at             timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT agreements_pkey PRIMARY KEY (id)
);

-- =====================================================================
-- financial_entries  (SHARED with the ERP totalmanagements)
--   * The full table is defined in the ERP schema; only divergences
--     from totalmanagements/schema_.sql are documented below.
--   * Reactstudio writes here with bu_code='REACT' only (see ADMIN_BU in
--     reactstudio/lib/admin-auth.ts).
-- =====================================================================
-- Divergence #1: project_id is NULLable in the live DB (the ERP snapshot says NOT NULL).
-- Divergence #2: the following columns exist in the live DB but are absent from totalmanagements/schema_.sql:
--   contract_id        bigint        REFERENCES public.contracts(id)
--   client_name        text
--   due_date           date
--   paid_at            timestamptz
--   payee_app_user_id  uuid          REFERENCES public.app_users(id)
--   approved_by        uuid          REFERENCES public.app_users(id)
--   approved_at        timestamptz
--   payment_ref        text
--
-- Indexes (live DB) related to reactstudio usage:
CREATE INDEX        financial_entries_contract_idx
  ON public.financial_entries (contract_id) WHERE (contract_id IS NOT NULL);
CREATE UNIQUE INDEX financial_entries_contract_installment_uq
  ON public.financial_entries (contract_id, category)
  WHERE (kind = 'revenue'::public.financial_kind
         AND contract_id IS NOT NULL
         AND status <> 'canceled'::public.financial_status
         AND category = ANY (ARRAY['deposit'::text, 'balance'::text]));
CREATE INDEX        financial_entries_kind_bu_due_idx
  ON public.financial_entries (kind, bu_code, due_date);
CREATE INDEX        idx_fe_due_date
  ON public.financial_entries (due_date) WHERE (kind = 'expense'::public.financial_kind);
CREATE INDEX        idx_fe_payee_app_user
  ON public.financial_entries (payee_app_user_id) WHERE (payee_app_user_id IS NOT NULL);

